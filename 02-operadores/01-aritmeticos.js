let a = 5, b = 7;

console.log(a + b); // Suma
console.log(a - b); // Resta
console.log(a * b); // Multiplicación
console.log(a / b); // División
console.log(a % b); // Módulo
console.log(a ** b); // Potencia

// Incrementar
console.log(++a); // Incrementa en 1 y luego muestra a
console.log(a++); // Muestra a  y luego incrementa en 1

// Decrementar
console.log(--b); // Decrementa en 1 y luego muestra b
console.log(b--); // Muestra b y luego decrementa en 1
